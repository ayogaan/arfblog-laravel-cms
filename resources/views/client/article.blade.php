<p style="text-align: justify;" >Visual Studio adalah IDE (Integeretd Development Environment) yang dibuat oleh Microsoft untuk pengembangan aplikasi berbasis .NET, VB, C#, C++, dan bahasa lainya seperti Nodejs, Python, dan Javascript.

    Intinya..
    
    IDE menyediakan segala hal yang kita butuhkan untuk membuat aplikasi. Sehingga tidak perlu lagi repot-repot install dan setup peralatan secara manual.
    
    Lalu..
    
    Apa bedanya dengan Visual Studio dengan Visual Studio Code?
    
    Visual Studio itu IDE, sedangkan Visual Studio Code (VS Code) adalah teks editor. Visual Studio saat ini hanya tersedia untuk Windows dan Mac saja, sedangkan VS Code tersedia untuk Linux, Mac, dan juga Windows.</p>


